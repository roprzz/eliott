// Turn a string into one dot grid: rows of booleans, glyphs separated by a blank column.
export function textToGrid(text) {
  const rows = 7
  const grid = Array.from({ length: rows }, () => [])
  for (const ch of text) {
    const glyph = GLYPHS[ch]
    if (!glyph) continue
    for (let r = 0; r < rows; r++) {
      if (grid[r].length > 0) grid[r].push(false) // spacing column
      for (const cell of glyph[r]) grid[r].push(cell === '#')
    }
  }
  return grid
}
